package cn.nvinfo.domain;
/**
 * 分销商页面返回产品所对应的图片
 */
public class Images {

	private String url;
	private String name;
	
	public Images() {
		super();
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
